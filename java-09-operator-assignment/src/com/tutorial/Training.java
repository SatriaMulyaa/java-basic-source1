package com.tutorial;

public class Training {
    public static void main(String[] args) {
        // assignment addition
        int a = 100;
        a+= 20;
        System.out.println(a);
        // assignment subtraction
        a-=20;
        System.out.println(a);
        // assignment multiplication
        a*=20;
        System.out.println(a);
        // assignment division
        a/=20;
        System.out.println(a);
        // assignment modulo
        a%=20;
        System.out.println(a);
    }
}
