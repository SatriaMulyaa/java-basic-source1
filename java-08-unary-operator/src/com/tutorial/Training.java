package com.tutorial;

public class Training {
    public static void main(String[] args) {
        // increment
        int a = 10;
        a++;
        System.out.println(a);
        // prefix increment
        int b = 5;
        System.out.printf("b = %d\n",++b);
        // post increment
        int c = 5;
        System.out.printf("c = %d\n",c++);
        // decrement
        a--;
        System.out.println(a);
        // prefix decrement
        System.out.printf("b = %d\n",--b);
        // post decrement
        System.out.printf("c = %d\n",c--);
        // boolean !
        boolean x = !true;
        System.out.println(x);

    }
}
