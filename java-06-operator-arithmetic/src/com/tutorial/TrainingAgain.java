package com.tutorial;

public class TrainingAgain {
    public static void main(String[] args) {
        int a = 100;
        int b = 10;

        // addition
        int c = a + b;
        System.out.printf("%d + %d = %d\n",a,b,c);
        // subtraction
        c = a - b;
        System.out.printf("%d - %d = %d\n",a,b,c);
        // division
        c = a / b;
        System.out.printf("%d / %d = %d\n",a,b,c);
        // multiplication
        c = a * b;
        System.out.printf("%d * %d = %d\n",a,b,c);
        // modulo
        c = a % b;
        System.out.printf("%d %% %d = %d\n",a,b,c);
    }
}
