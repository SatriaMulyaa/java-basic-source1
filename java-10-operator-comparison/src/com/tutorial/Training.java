package com.tutorial;

public class Training {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        //equal
        System.out.println(a == b);
        // not equal
        System.out.println(a!=b);
        // greater than
        System.out.println(a>b);
        // less than
        System.out.println(a<b);
        // greater than or equal
        System.out.println(a<=b);
        // less than or equal
        System.out.println(a>=b);
    }
}
